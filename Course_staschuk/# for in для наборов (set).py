# for in для наборов (set)
video_ids = {1435, 4317, 2761, 5721}#неупорядочненная последовательность
for id in video_ids:
    print(id)

# for in для строк
my_name = 'Andrew'
for char in my_name:
    print(char)

# for in для диапазонов
for num in range(5):
    print(num)

for odd_num in range(3, 10, 2):#с указанием шага и конечного числа
    print(odd_num)

